class Iceberg():                                     
    """                                                                 
    Function for getting mass of 'blocky' (cuboid) icebergs.            
    """                                                                 
    length = 72
    width = 72
    height = 22
    densityIce = 920  # kg/m^3                                          
    mass = densityIce * length * width * height  # kilograms 
